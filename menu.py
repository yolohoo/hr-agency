def get_user_choice(max_choice):
    """Получает и валидирует выбор пользователя в меню."""
    while True:
        try:
            choice = int(input(f"Выберите пункт меню (1-{max_choice}): "))
            if 1 <= choice <= max_choice:
                return choice
            print(f"Ошибка: Введите число от 1 до {max_choice}.")
        except ValueError:
            print("Ошибка: Введите корректное число.")
